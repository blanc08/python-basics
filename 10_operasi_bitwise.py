# Jika di bahasa indonesiakan menjadi "operasi masing masing bit"

a = 10
b = 50

# OR
hasil_or = a | b
print("===============OR=========")
print("biner a =", format(a, "08b"), " , dengan nilai =", a)
print("biner b =", format(b, "08b"), " , dengan nilai =", b)
print("biner or =", format(hasil_or, "08b"), " , dengan nilai =", hasil_or)

# AND
AND = a & b
print("===============AND=========")
print("biner a =", format(a, "08b"), " , dengan nilai =", a)
print("biner b =", format(b, "08b"), " , dengan nilai =", b)
print("biner AND =", format(AND, "08b"), " , dengan nilai =", AND)

# XOR
XOR = a ^ b
print("===============XOR=========")
print("biner a =", format(a, "08b"), " , dengan nilai =", a)
print("biner b =", format(b, "08b"), " , dengan nilai =", b)
print("biner XOR =", format(XOR, "08b"), " , dengan nilai =", XOR)

# NOT | membalik biner
NOT = ~a
print("===============NOT=========")
print("biner a =", format(a, "08b"), " , dengan nilai =", a)
print("biner NOT =", format(NOT, "08b"), " , dengan nilai =", NOT)

