# Operator ditambah assignment

# assignment
x = 5
print(x)

# ditambah operator +
x += 1
print(x)

# ditambah operator -
x -= 1
print(x)

# ditambah operator *
x *= 186
print(x)

# ditambah operator /
x = 5
x /= 3
print(x)

# ditambah operator %
x = 5
x %= 3
print(x)

# ditambah operator //
x = 5
x //= 2
print(x)

# ditambah operator **
x = 5
x **= 3
print(x)


# Operator Bitwise
# OR
y = True
y |= False
print(y)

# AND
y = True
y &= False
print(y)

# XOR
y = True
y ^= False
print(y)

# geser atau shift
d = 0b0100
print("nilai dari d =", format(d, "04b"))
d >>= 2
print("nilai dari d =", format(d, "04b"))
d <<= 2
print("nilai dari d =", format(d, "04b"))
