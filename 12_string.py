# single quote
variable_string = "ini string dengan single quote"
print(variable_string)

# dua duanya
variable_string = "ini string dengan double quote quote dan 'single quote'"
print(variable_string)

# kebalikan dari yang atas
variable_string = 'ini string dengan single quote quote dan "double quote"'
print(variable_string)

# menggunakan escape (\)
variable_string = 'echo bilang "jangan takut inikan hari jum\'at"'
print(variable_string)

# backslash \
variable_string = "contoh Backslash \\"
print(variable_string)

# tab (\t)
variable_string = "contoh tab \t kaya gini"
print(variable_string)

# backspace (\b)
variable_string = "contoh backspace \bkaya gini"
print(variable_string)

# newline (\n) | LF => Line feed || untuk linux dan macos biasanya
variable_string = "ini baris pertama\nini baris kedua"
print(variable_string)

# newline (\r) | CR => Carriage return || OS lama kaya commodore, acorn, dll
variable_string = "ini baris pertama\rini baris kedua"
print(variable_string)

# newline (\r\n) | CRLF => Line feed Carriage return || auto
variable_string = "ini baris pertama\r\nini baris kedua"
print(variable_string)

# string literal atau raw string
variable_string = r"C:/new folder"
print(variable_string)

# multiline
