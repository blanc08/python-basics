# loops

# range
for i in range(5):
    print(f" i sekarang -> {i}")
print("=========================")

# list
list_angka = [0, 1, 2, 3, 4, 5]
for i in list_angka:
    print(f" i sekarang -> {i}")
print("=========================")

# string
string_variable = "ini string variable"
for huruf in string_variable:
    print(huruf)
print("=========================")

# while loop
i = 1
while i <= 10:
    print(f" i sekarang -> {i}")
    i += 1
print("=========================")
