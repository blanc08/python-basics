# continue, break and pass

# continue
for i in range(5):
    if i == 3:
        continue

    print(f" i sekarang -> {i}")
print("=========================")


# break
for i in range(5):
    if i == 3:
        break

    print(f" i sekarang -> {i}")
print("=========================")

# pass
for i in range(5):
    pass


print("=========================")
