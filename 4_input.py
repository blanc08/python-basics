# menangkap input dari terminal
hasil = input("masukkan sesuatu : ")

# tipe data yang dihasilkan adalah str atau string
print("hasil = ", hasil, ", tipe = ", type(hasil))

# jika ingin mengambil integer maka perlu di casting
hasil_integer = int(input("masukkan angka : "))
print("hasil = ", hasil_integer, ", tipe = ", type(hasil_integer))
