x = 10
y = 3

# tambah | +
hasil_tambah = x + y
print("hasil_tambah = ", hasil_tambah)

# kurang | -
hasil_kurang = x - y
print("hasil_kurang = ", hasil_kurang)

# bagi | / | otomatis ke convert jadi float jika hasilnya berkoma
hasil_bagi = x / y
print("hasil_bagi = ", hasil_bagi, ", tipe = ", type(hasil_bagi))

# kali | *
hasil_kali = x * y
print("hasil_kali = ", hasil_kali)

# pangkat atau power | **
hasil_pangkat = x**y
print("hasil_pangkat = ", hasil_pangkat)

# modulus | %
hasil_modulus = x % y
print("hasil_modulus = ", hasil_modulus)

# floor division | pembulatan dari hasil bagi | //
hasil_floor_division = x // y
print("hasil_floor_division = ", hasil_floor_division)